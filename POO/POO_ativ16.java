/*Padrão Singleton
O Padrão Singleton tem como definição garantir que uma classe tenha apenas uma 
instância de si mesma e que forneça um ponto global de acesso a ela. Ou seja,
 uma classe gerencia a própria instância dela além de evitar que qualquer outra 
 classe crie uma instância dela. Para criar a instancia tem-se que passar pela 
 classe obrigatoriamente, nenhuma outra classe pode instanciar ela. O Padrão 
 Singleton também oferece um ponto global de acesso a sua instância. 
 A própria classe sempre vai oferecer a própria instância dela e caso não tenha
 ainda uma instância, então ela mesma cria e retorna essa nova instância criada.
*/